def run_text(command: list[str], timeout: int = 180) -> str:
    try:
        result = subprocess.run(
            command,
            check=False,
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True,
            timeout=timeout,
        )
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return ""
    return result.stdout


def pdf_pages(path: Path) -> int:
    info = run_text(["pdfinfo", str(path)], timeout=30)
    for line in info.splitlines():
        if line.startswith("Pages:"):
            value = line.split(":", 1)[1].strip()
            return int(value) if value.isdigit() else 0
    return 0


def pdf_text(path: Path) -> str:
    return run_text(["pdftotext", "-layout", str(path), "-"], timeout=420)


def normalize_space(value: str) -> str:
    return re.sub(r"\s+", " ", value).strip()


def extract_caption_rows(
    relative_path: str, text: str, max_per_pdf: int = 14
) -> tuple[list[dict[str, object]], int, int]:
    caption_rows: list[dict[str, object]] = []
    lines = [normalize_space(line) for line in text.splitlines()]
    pattern = re.compile(r"^(Figure|Fig\.|Table|Tabla)\s+[A-Z]?\d+[\.: -]+(.+)", re.IGNORECASE)
    figure_count = 0
    table_count = 0
    seen: set[str] = set()
    for line in lines:
        match = pattern.match(line)
        if not match:
            continue
        caption_type = (
            "table" if match.group(1).lower().startswith(("table", "tabla")) else "figure"
        )
        if caption_type == "figure":
            figure_count += 1
        else:
            table_count += 1
        caption = normalize_space(line)
        dedupe_key = f"{caption_type}:{caption[:180]}"
        if dedupe_key in seen or len(caption) < 20:
            continue
        seen.add(dedupe_key)
        if len(caption_rows) < max_per_pdf:
            caption_rows.append(
                {
                    "relative_path": relative_path,
                    "caption_type": caption_type,
                    "caption_index": len(caption_rows) + 1,
                    "caption_text": caption,
                    "caption_index_truncated": "false",
                }
            )
    if len(seen) > max_per_pdf:
        caption_rows.append(
            {
                "relative_path": relative_path,
                "caption_type": "truncation_note",
                "caption_index": max_per_pdf + 1,
                "caption_text": f"{len(seen) - max_per_pdf} additional figure/table captions omitted from this compact index.",
                "caption_index_truncated": "true",
            }
        )
    return caption_rows, figure_count, table_count


def write_csv(path: Path, rows: list[dict[str, object]], fields: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, lineterminator="\n")
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in fields})


def build_curated_visual_rows(caption_rows: list[dict[str, object]]) -> list[dict[str, object]]:
    lookup = {
        (
            str(row["relative_path"]),
            str(row["caption_type"]),
            int(str(row["caption_index"])),
        ): row
        for row in caption_rows
        if str(row.get("caption_index", "")).isdigit()
    }

    curated: list[dict[str, object]] = []
    missing: list[str] = []
    for spec in CURATED_VISUAL_SPECS:
        key = (
            str(spec["relative_path"]),
            str(spec["caption_type"]),
            int(str(spec["caption_index"])),
        )
        caption = lookup.get(key)
        if caption is None:
            missing.append(f"{key[0]} / {key[1]} #{key[2]}")
            continue
        curated.append(
            {
                "relative_path": caption["relative_path"],
                "caption_type": caption["caption_type"],
                "caption_index": caption["caption_index"],
                "caption_text": caption["caption_text"],
                "editorial_sink": spec["editorial_sink"],
                "why_useful": spec["why_useful"],
                "use_rule": spec["use_rule"],
                "claim_boundary": spec["claim_boundary"],
            }
        )

    if missing:
        raise SystemExit("Missing curated caption rows: " + "; ".join(missing))
    return curated


def build_rows(source_dir: Path) -> tuple[list[dict[str, object]], list[dict[str, object]]]:
    pdfs = sorted(path for path in source_dir.rglob("*.pdf") if path.is_file())
    rows: list[dict[str, object]] = []
    caption_rows: list[dict[str, object]] = []

    missing = [
        str(path.relative_to(source_dir))
        for path in pdfs
        if str(path.relative_to(source_dir)) not in REVIEWS
    ]
    extra = [key for key in REVIEWS if not (source_dir / key).exists()]
    if missing or extra:
        message = []
        if missing:
            message.append("Missing curated reviews for: " + ", ".join(missing))
        if extra:
            message.append("Curated reviews without PDF: " + ", ".join(extra))
        raise SystemExit("\n".join(message))

    for path in pdfs:
        relative_path = str(path.relative_to(source_dir))
        review = REVIEWS[relative_path]
        text = pdf_text(path)
        words = len(re.findall(r"\b\w+\b", text))
        captions, figure_count, table_count = extract_caption_rows(relative_path, text)
        caption_rows.extend(captions)
        rows.append(
            {
                "folder": Path(relative_path).parts[0],
                "file": path.name,
                "relative_path": relative_path,
                "title": review.title,
                "authors": review.authors,
                "year": review.year,
                "status": review.status,
                "primary_domain": review.primary_domain,
                "bib_key": review.bib_key,
                "bib_status": review.bib_status,
                "canonical_url": review.canonical_url,
                "verification_status": review.verification_status,
                "pages": pdf_pages(path),
                "word_count": words,
                "figure_caption_count": figure_count,
                "table_caption_count": table_count,
                "core_concepts": review.core_concepts,
                "key_claims": review.key_claims,
                "method_or_evidence": review.method_or_evidence,
                "conclusions": review.conclusions,
                "figures_tables_useful": review.figures_tables_useful,
                "limitations": review.limitations,
                "crpto_value": review.crpto_value,
                "extended_lab_value": review.extended_lab_value,
                "quarto_destination": review.quarto_destination,
                "decision": review.decision,
                "action_required": review.action_required,
                "evidence_gate": review.evidence_gate,
                "artifact_sink": review.artifact_sink,
                "stop_rule": review.stop_rule,
                "implementation_or_experiment": review.implementation_or_experiment,
                "notes": review.notes,
                "tags": ";".join(review.tags),
            }
        )
    return rows, caption_rows


def markdown_table(
    rows: list[dict[str, object]], fields: list[str], max_rows: int | None = None
) -> str:
    selected = rows[:max_rows] if max_rows is not None else rows
    header = "| " + " | ".join(fields) + " |"
    divider = "| " + " | ".join("---" for _ in fields) + " |"
    body = []
    for row in selected:
        body.append(
            "| " + " | ".join(str(row.get(field, "")).replace("|", "/") for field in fields) + " |"
        )
    return "\n".join([header, divider, *body])


